package Conceptos_Basicos;

public class Tipos {
    public static void main(String[] args) {

        // 1 Num√©ricos
        // Enteros
        byte variable1  = 5;
        short variable2 = 50;
        int variable3 = 500;
        long variable4 = 5000;

        System.out.println("Tipos de datos enteros: " + variable1 + " , " + variable2 + " , " + variable3 + " , " + variable4);

        // Decimales
        float variable5 = 1.5f;
        double variable6 = 10.5d;

        System.out.println("Tipos de datos decimales: " + variable5 + " , " + variable6);


        //2 Booleanos
         boolean variable7 = true;
         boolean variable8 = false;

        System.out.println("Tipos de datos booleanos: " + variable7 + " , " + variable8);

        //3 texto
        char variable9 = 'a';
        String variable10 = "Hola Mundo";

        System.out.println("Tipos de datos de Texto: " + variable9 + " , " + variable10);



    }
}
